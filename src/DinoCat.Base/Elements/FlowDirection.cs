namespace DinoCat.Elements
{
    /// <summary>
    /// Defines the direction that content should be layed out.
    /// </summary>
    public enum FlowDirection
    {
        /// <summary>
        /// Content should flow from left to right.
        /// </summary>
        LeftToRight,
        /// <summary>
        /// Content should flow from right to left.
        /// </summary>
        RightToLeft
    }
}
